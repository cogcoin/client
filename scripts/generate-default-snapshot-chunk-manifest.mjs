#!/usr/bin/env node

import { createHash } from "node:crypto";
import { createReadStream } from "node:fs";
import { mkdir, readFile, stat, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const SCRIPT_PATH = fileURLToPath(import.meta.url);
const PROJECT_ROOT = resolve(dirname(SCRIPT_PATH), "..");
const DEFAULT_OPTIONS = {
  input: resolve(PROJECT_ROOT, "..", "..", "bitcoin-extract", "utxo", "utxo-910000.dat"),
  output: resolve(PROJECT_ROOT, "src", "bitcoind", "bootstrap", "default-snapshot-chunk-manifest.ts"),
  snapshotFilename: "utxo-910000.dat",
  snapshotHeight: 910000,
  snapshotSizeBytes: 9637809744,
  snapshotSha256: "6ac0208110d6d6c0783c50ea825aae32f5229cf1dcb63ac986543e95aa0306bf",
  chunkSizeBytes: 4 * 1024 * 1024,
  formatVersion: 1,
  check: false,
};

function parseArgs(argv) {
  const options = { ...DEFAULT_OPTIONS };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];

    if (arg === "--check") {
      options.check = true;
      continue;
    }

    if (!arg.startsWith("--")) {
      throw new Error(`unknown_argument_${arg}`);
    }

    const key = arg.slice(2);
    const value = argv[index + 1];

    if (value === undefined) {
      throw new Error(`missing_value_for_${arg}`);
    }

    index += 1;

    switch (key) {
      case "input":
      case "output":
      case "snapshot-filename":
      case "snapshot-sha256":
        options[toCamelCase(key)] = value;
        break;
      case "snapshot-height":
      case "snapshot-size-bytes":
      case "chunk-size-bytes":
      case "format-version":
        options[toCamelCase(key)] = Number(value);
        break;
      default:
        throw new Error(`unknown_argument_${arg}`);
    }
  }

  return options;
}

function toCamelCase(value) {
  return value.replace(/-([a-z])/g, (_match, char) => char.toUpperCase());
}

async function buildManifest(options) {
  const inputPath = resolve(options.input);
  const info = await stat(inputPath);

  if (info.size !== options.snapshotSizeBytes) {
    throw new Error(`snapshot_size_mismatch_${info.size}`);
  }

  const chunkSha256s = [];
  const wholeHash = createHash("sha256");
  let currentChunkHash = createHash("sha256");
  let currentChunkBytes = 0;
  let processedBytes = 0;

  for await (const chunk of createReadStream(inputPath)) {
    wholeHash.update(chunk);
    let offset = 0;

    while (offset < chunk.length) {
      const remainingChunkBytes = options.chunkSizeBytes - currentChunkBytes;
      const writeLength = Math.min(remainingChunkBytes, chunk.length - offset);
      const segment = chunk.subarray(offset, offset + writeLength);

      currentChunkHash.update(segment);
      currentChunkBytes += segment.length;
      processedBytes += segment.length;
      offset += writeLength;

      if (currentChunkBytes === options.chunkSizeBytes || processedBytes === options.snapshotSizeBytes) {
        chunkSha256s.push(currentChunkHash.digest("hex"));
        currentChunkHash = createHash("sha256");
        currentChunkBytes = 0;
      }
    }
  }

  if (processedBytes !== options.snapshotSizeBytes) {
    throw new Error(`snapshot_size_mismatch_${processedBytes}`);
  }

  const wholeSha256 = wholeHash.digest("hex");

  if (wholeSha256 !== options.snapshotSha256) {
    throw new Error(`snapshot_sha256_mismatch_${wholeSha256}`);
  }

  return {
    formatVersion: options.formatVersion,
    chunkSizeBytes: options.chunkSizeBytes,
    snapshotFilename: options.snapshotFilename,
    snapshotHeight: options.snapshotHeight,
    snapshotSizeBytes: options.snapshotSizeBytes,
    snapshotSha256: wholeSha256,
    chunkSha256s,
  };
}

function renderManifestSource(manifest) {
  return `import type { SnapshotChunkManifest } from "../types.js";

// Generated by scripts/generate-default-snapshot-chunk-manifest.mjs.
export const DEFAULT_SNAPSHOT_CHUNK_MANIFEST: SnapshotChunkManifest = ${JSON.stringify(manifest, null, 2)} as const;
`;
}

async function main() {
  const options = parseArgs(process.argv.slice(2));
  const manifest = await buildManifest(options);
  const outputPath = resolve(options.output);
  const source = renderManifestSource(manifest);

  if (options.check) {
    const current = await readFile(outputPath, "utf8");

    if (current !== source) {
      throw new Error("snapshot_chunk_manifest_out_of_date");
    }

    return;
  }

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, source);
}

main().catch((error) => {
  const message = error instanceof Error ? error.message : String(error);
  process.stderr.write(`${message}\n`);
  process.exitCode = 1;
});
